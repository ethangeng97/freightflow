let _role = "admin"; // default English
let _supplierCnMap = {}; // english name → chinese name
let _customerShortMap = {}; // 委托单位 full name → name_short

// 用户手动选择的语言（覆盖角色默认）。'zh' | 'en' | null
const LANG_OVERRIDE_KEY = "ff_lang_override";
let _langOverride = null;
try {
  const v = typeof localStorage !== "undefined" && localStorage.getItem(LANG_OVERRIDE_KEY);
  if (v === "zh" || v === "en") _langOverride = v;
} catch {}

export function setI18nRole(role) {
  _role = role;
}

// 设置语言覆盖；null 表示恢复角色默认
export function setLangOverride(lang) {
  _langOverride = (lang === "zh" || lang === "en") ? lang : null;
  try {
    if (_langOverride) localStorage.setItem(LANG_OVERRIDE_KEY, _langOverride);
    else localStorage.removeItem(LANG_OVERRIDE_KEY);
  } catch {}
}
export function getLangOverride() { return _langOverride; }

// 当前生效语言
function currentLang() {
  if (_langOverride) return _langOverride;
  return ["operator","sales","supplier","finance"].includes(_role) ? "zh" : "en";
}

export function setSupplierCnMap(map) {
  _supplierCnMap = map || {};
}

// Map from customers.name (full 委托单位 name) to customers.name_short.
// Used to render portal "Supplier" as the short name of shipments.customer.
export function setCustomerShortMap(map) {
  _customerShortMap = map || {};
}

// Resolve portal's "Supplier" display: name_short of shipments.customer.
// Falls back to the raw shipments.supplier value (post-backfill) when the
// customer is missing from the dictionary.
export function tCustomerShort(customerName, supplierFallback) {
  if (customerName && _customerShortMap[customerName]) return _customerShortMap[customerName];
  return supplierFallback || null;
}

export function t(key) {
  if (currentLang() === "zh") return zh[key] || key;
  return key;
}

// 供应商中文名（仅 zh 模式）
export function tSupplier(name) {
  if (!name) return null;
  if (currentLang() === "zh") return _supplierCnMap[name] || name;
  return name;
}

export function isZh() {
  return currentLang() === "zh";
}
